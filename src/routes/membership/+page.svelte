<h1>Membership</h1>

<p>All players are required to be members. Players may attend club nights as a guest member for up to 6 visits before joining.</p>

<p>The club offers adult and junior memberships. To qualify for junior membership an individual must be 18 or under on 1st September 2024.</p>

<p>
  Membership costs £30 per year for adults and is free for juniors. Members registered with other clubs or on the coaching register are entitled to a 50%
  reduction in the membership fee. There is a fee of £6 for each session attended (club night or match). All members can pay a fixed £140 per year for all club
  night fees.
</p>

<p>
  The membership fee covers individual affiliation to Badminton England and club affiliation to Badminton England, Oxfordshire Badminton Association and Banbury
  Town Badminton League. As a member of Badminton England individuals benefit from £10M public liability insurance cover, a monthly newsletter, discounts on
  badminton purchases from retailers, discounts on Badminton England events and access to Badminton England leagues.
</p>

<p>
  The membership year runs from September to August and there are approximately 40 club nights per year. Annual membership fees are due on 1st September. Fees
  will be calculated pro rata for those joining part way through the membership year.
</p>
